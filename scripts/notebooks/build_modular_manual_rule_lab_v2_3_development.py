def restore_files() -> list[str]:
    restored = []
    archive = zipfile.ZipFile(io.BytesIO(base64.b64decode(PAYLOAD)))
    for info in archive.infolist():
        target = PROJECT_ROOT / info.filename
        target.parent.mkdir(parents=True, exist_ok=True)
        target.write_bytes(archive.read(info.filename))
        restored.append(str(target))
    return restored

def forbidden_bundle_scan(bundle: Path) -> dict:
    forbidden_terms = ['prediction.csv', 'predictions.csv', 'ocr_frame', 'raw_audio', 'raw_scene', 'media', 'video', 'frame', 'cache', 'model', 'raw video', 'proxy', 'checkpoint']
    hits = []
    for path in bundle.rglob('*'):
        if path.is_file():
            rel = str(path.relative_to(bundle)).lower()
            if path.suffix.lower() == '.csv' or any(term in rel for term in forbidden_terms):
                hits.append(str(path.relative_to(bundle)))
    return {'clean': len(hits) == 0, 'hits': hits, 'file_count': sum(1 for p in bundle.rglob('*') if p.is_file())}

