def write_latest_bundle() -> dict:
    if LATEST_BUNDLE.exists():
        shutil.rmtree(LATEST_BUNDLE)
    LATEST_BUNDLE.mkdir(parents=True, exist_ok=True)
    bundle_files = [SCRIPT_PATH, SUMMARY_PATH, REPORT_PATH, RUN_LOG_PATH]
    bundle_files += sorted(MODULAR_ROOT.glob('*.ipynb'))
    bundle_files += [MODULAR_ROOT / 'common/manual_rule_lab_common.py', MODULAR_ROOT / 'config/manual_rule_lab_v2_3_config.json']
    for src in bundle_files:
        if src.exists():
            dest = LATEST_BUNDLE / src.relative_to(PROJECT_ROOT)
            dest.parent.mkdir(parents=True, exist_ok=True)
            shutil.copy2(src, dest)
    return forbidden_bundle_scan(LATEST_BUNDLE)

def main() -> int:
    restored = restore_files()
    report = write_reports(restored)
    scan = write_latest_bundle()
    report['latest_bundle'] = str(LATEST_BUNDLE)
    report['latest_bundle_forbidden_scan'] = scan
    report['ready_for_manual_rule_tuning'] = scan['clean']
    REPORT_PATH.write_text(json.dumps(report, ensure_ascii=False, indent=2), encoding='utf-8')
    shutil.copy2(REPORT_PATH, LATEST_BUNDLE / REPORT_PATH.name)
    print(json.dumps({'restored_file_count': len(restored), 'latest_bundle': str(LATEST_BUNDLE), 'forbidden_scan_clean': scan['clean']}, ensure_ascii=False, indent=2))
    return 0 if scan['clean'] else 1

